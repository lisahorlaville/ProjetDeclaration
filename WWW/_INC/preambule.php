<h2>Préambule</h2>
<p>

Considérant que la connaissance de la dignité inhérente à tous les membres de la famille humaine et de leurs droits égaux et inaliénables constituent le fondement de la liberté, de la justice et de la paix dans le monde,
</br></br>
Considérant que la méconnaissance et le mépris des droits de l’homme ont conduit à des actes de barbarie qui révoltent la conscience de l’humanité et que l’avènement d’un monde où les êtres humains seront libres de parler et de croire, libérés de la terreur et de la misère, a été proclamé comme la plus haute aspiration de l’homme,
</br></br>
Considérant qu’il est essentiel que les droits de l’homme soient protégés par un régime de droit pour que l’homme ne soit pas contraint, en suprême recours, à la révolte contre la tyrannie et l’oppression,
<br></br>
Considérant qu’il est essentiel d’encourager le développement de relations amicales entre nations,
</br></br>
Considérant que dans la Charte les peuples des Nations unies ont proclamé à nouveau leur foi dans les droits fondamentaux de l’homme, dans la dignité et la valeur de la personne humaine, dans l’égalité des droits des hommes et des femmes, et qu’ils sont déclarés résolus à favoriser le progrès social et à instaurer de meilleures conditions de vie dans une liberté plus grande,
</br></br>
Considérant que les États membres se sont engagés à assurer, en coopération avec l’Organisation des Nations unies, le respect universel et effectif des droits de l’homme et des libertés fondamentales,
</br></br>
Considérant qu’une conception commune de ces droits et libertés est de la plus haut importance pour remplir pleinement cet engagement,
</br></br>
L’assemblée générale proclame la présente Déclaration universelle des droits de l’homme comme l’idéal commun à atteindre par tous les peuples et toutes les nations afin que tous les individus et tous les organes de la société, ayant cette Déclaration constamment à l’esprit, s’efforcent, par l’enseignement et l’éducation, de développer le respect de ces droits et libertés et d’en assurer, par des mesures progressives d’ordre national et international, la reconnaissance et l’application universelles et effectives, tant parmi les populations des États membres eux-mêmes que parmi celles de territoires placés sous leur juridiction.
</p>
