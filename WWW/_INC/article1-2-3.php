<div class="article1">
<h2>Art. Premier</h2>
<p>
Tous les êtres humains naissent libres et égaux en dignité et en droits.
Ils sont doués de raison et de conscience et doivent agir les uns envers les autres dans un esprit de fraternité.
</p>
</div>

<div class="article2">
<h2>Art. 2</h2>
<p>
Chacun peut se prévaloir de tous les droits et de toutes les libertés proclamés dans la présente Déclaration,
sans distinction aucune, notamment de race, de couleur, de sexe, de langue, de religion,
d'opinion politique ou de toute autre opinion, d'origine nationale ou sociale, de fortune,
de naissance ou de toute autre situation.
De plus, il ne sera fait aucune distinction fondée sur le statut politique, juridique ou international du pays
ou du territoire dont une personne est ressortissante, que ce pays ou territoire soit indépendant, sous tutelle,
non autonome ou soumis à une limitation quelconque de souveraineté.
</p>
</div>

<div class="article3">
<h2>Art. 3</h2>
<p>
Tout individu a droit à la vie, à la liberté et à la sûreté de sa personne.
</p>
</div>

