<div class=article7>
	<h2>Art. 7</h2>
	<p>
		Tous sont égaux devant la loi et ont droit sans distinction à une égale protection de la loi. Tous ont droits à une protection égale contre toute discrimination qui violerait la présente Déclaration et contre toute provocation à une telle discrimination.
	</p>
</div>

<div class=article8>
	<h2>Art. 8</h2>
	<p>
		Toute personne a droit à un recours effectif devant les juridictions nationales compétentes contre les actes violant les droits fondamentaux qui lui sont reconnus par la constitution ou par la loi.
	</p>
</div>

</div>
