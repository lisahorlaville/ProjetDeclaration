<div class=article25>
	<h2>Art. 25</h2>
	<p>
		<strong>1.</strong> Toute personne à droit à un niveau de vie suffisant pour assurer sa santé, son bien-être et ceux de sa famille, notamment pour l’alimentation, l’habillement, le logement, les soins médicaux ainsi que pour les services sociaux nécessaires ; elle a droit à la sécurité en cas de chômage, de maladie, d’invalidité, de veuvage, de vieillesse ou dans les autres cas de perte de ses moyens de subsistance par suite de circonstances indépendantes de sa volonté.
		<br/><br/>
		<strong>2.</strong> La maternité et l’enfance ont droit à une aide et à une assistance spéciale. Tous les enfants qu’ils soient dans le mariage ou hors mariage, jouissent de la même protection sociale.
	</p>
</div>

<div class=article26>
	<h2>Art. 26</h2>
	<p>
		<strong>1.</strong> Toute personne à droit à l’éducation. L’éducation doit être gratuite, au moins en ce qui concerne l’enseignement élémentaire et fondamental. L’enseignement élémentaire est obligatoire. L’enseignement technique et professionnel doit être généralisé ; l’accès au études supérieures doit être ouvert en pleine égalité à tous en fonction de leur mérite.
		<br/><br/>
		<strong>2.</strong> L’éducation doit viser au plein épanouissement de la personnalité humaine et au renforcement du respect des droits de l’homme et des libertés fondamentales. Elle doit favoriser la compréhension, la tolérance et l’amitié entre toutes les nations et tous les groupes raciaux ou religieux, ainsi que pour le développement des activités des Nations unies pour le maintien de la paix.
		<br/><br/>
		<strong>3.</strong> Les parent sont, par priorité, le droit de choisir le genre d’éducation a donner à leurs enfants.
	</p>
</div>

