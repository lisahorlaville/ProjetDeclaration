<span class=article12>
	<h2>Art. 12</h2>
	<p>
		Nul ne sera l'objet d'immixtions arbitraires dans sa vie privée, sa famille, son domicile ou sa correspondance, ni d'atteintes à son honneur et à sa réputation. Toute personne a droit à la protection de la loi contre de telles immixtions ou de telles atteintes. 
	</p>
</span>

<span class=article13>
	<h2>Art. 13</h2>
	<p>
		<strong>1.</strong> Toute personne a le droit de circuler librement et de choisir sa résidence à l'intérieur d'un État. 
		<br/><br/>
		<strong>2.</strong> Toute personne a le droit de quitter tout pays, y compris le sien, et de revenir dans son pays.
	</p>
</span>

<span class=article14>
	<h2>Art. 14</h2>
	<p>
		 <strong>1.</strong> Devant la persécution, toute personne a le droit de chercher asile et de bénéficier de l'asile en d'autres pays.
		 <br/><br/>
		 <strong>2.</strong> Ce droit ne peut être invoqué dans le cas de poursuites réellement fondées sur un crime de droit commun ou sur des agissements contraires aux buts et aux principes des Nations Unies.
	</p>
</span>

</div>
</section>