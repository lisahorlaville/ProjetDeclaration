<div class=article15>
	<h2>Art. 15</h2>
	<p>
		<strong>1.</strong> Tout individu a droit à une nationalité.
		<br/><br/>
		<strong>2.</strong> Nul ne peut être arbitrairement privé de sa nationalité, ni du droit de changer de nationalité.
	</p>
</div>

<div class=article16>
	<h2>Art. 16</h2>
	<p>
		<strong>1.</strong> À partir de l'âge nubile, l'homme et la femme, sans aucune restriction quant à la race, la nationalité ou la religion, ont le droit de se marier et de fonder une famille. Ils ont des droits égaux au regard du mariage, durant le mariage et lors de sa dissolution.
		<br/><br/>
		<strong>2.</strong> Le mariage ne peut être conclu qu'avec le libre et plein consentement des futurs époux.
		<br/><br/>
		<strong>3.</strong> La famille est l'élément naturel et fondamental de la société et a droit à la protection de la société et de l'État.
	</p>
</div>

