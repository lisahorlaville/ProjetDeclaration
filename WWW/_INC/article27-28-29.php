<div class=article27>
	<h2>Art. 27</h2>
	<p>
		<strong>1.</strong> Toute personne à la droit de prendre part librement à la vie culturelle de la communauté, de jouir des arts et de participer au progrès scientifique et aux bienfaits qui en résultent.
		<br/><br/> 
		<strong>2.</strong> Chacun à droit à la protection des intérêts moraux et matériels découlant de toutes production scientifique, littéraire ou artistique dont il est l’auteur.
	</p>
</div>

<div class=article28>
	<h2>Art. 28</h2>
	<p>
		Toute personne à droit à ce que règne, sur le plan social et sur le plan international, un ordre tels que les droits et libertés énoncées dans la présente Déclaration puissent y trouver plein effet.
	</p>
</div>

<div class=article29>
	<h2>Art. 29</h2>
	<p>
		<strong>1.</strong> L’individu à des devoirs envers la communauté dans laquelle seule le libre et plein développement de sa personnalité est possible.
		<br/><br/> 
		<strong>2.</strong> Dans l’exercice de ses droits et dans la jouissance de ses libertés, chacun n’est soumis qu’aux limitations établies par la loi exclusivement en vue d’assurer la reconnaissance et le respect des droits et libertés d’autrui et afin de satisfaire aux justes exigences de la morale, de l’ordre public et du bien être général dans une société démocratique.
		<br/><br/>
		<strong>3.</strong> Ces droits et libertés ne pourront, en aucun cas, s’exercer contrairement au buts et aux principes des Nations unies.
	</p>
</div>

