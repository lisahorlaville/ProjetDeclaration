<div class=article22>
	<h2>Art. 22</h2>
	<p>
		Toute personne, en tant que membre de la société, a droit à la sécurité sociale ; elle est fondé à obtenir la satisfaction des droits économiques, sociaux et culturels indispensables à sa dignité et au libre développement de sa personnalité, grâce à l’effort national et à la coopération internationale, compte tenue l’organisation et des ressources de chaque pays.
	</p>
</div>

<div class=article23>
	<h2>Art. 23</h2>
	<p>
		<strong>1.</strong> Toute personne à droit au travail, au libre choix de son travail, à des conditions équitables et satisfaisantes de travail et à la protection contre le chômage. 
		<br/><br/>
		<strong>2.</strong> Tous ont droit, sans aucune discrimination, à un salaire égal pou un travail égal. 
		<br/><br/>
		<strong>3.</strong> Quiconque travail à droit à une rémunération équitable satisfaisante lui assurant ainsi qu’à sa famille une existence conforme à la dignité humaient complétée, s’il y a lieu, par tous autres moyens de protection sociale. 
		<br/><br/>
		<strong>4.</strong> Toutes personnes à la droit de fonder avec d’autres des syndicats et de s’affilier à des syndicats pour la défense de ses interêts.
	</p>
</div>

<div class=article24>
	<h2>Art. 24</h2>
	<p>
		Toute personne à droit au repos et aux loisirs et notamment à une limitation raisonnable de la durée du travail et à des congés payer périodiques.
	</p>
</div>


